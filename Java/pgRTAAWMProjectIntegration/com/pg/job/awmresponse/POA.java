package com.pg.job.awmresponse;

public class POA {
	  
	  private String taskName;
	  private String taskDelvierable;
	  private String errorCode;
	  private String errorMessage;
	  private String warningMessage;
	  private String statusTask;
	 
	public String getTaskName() {
		return taskName;
	}
	public void setTaskName(String taskName) {
		this.taskName = taskName;
	}
	public String getTaskDelvierable() {
		return taskDelvierable;
	}
	public void setTaskDelvierable(String taskDelvierable) {
		this.taskDelvierable = taskDelvierable;
	}
	public String getErrorCode() {
		return errorCode;
	}
	public void setErrorCode(String errorCode) {
		this.errorCode = errorCode;
	}
	public String getErrorMessage() {
		return errorMessage;
	}
	public void setErrorMessage(String errorMessage) {
		this.errorMessage = errorMessage;
	}
	public String getWarningMessage() {
		return warningMessage;
	}
	public void setWarningMessage(String warningMessage) {
		this.warningMessage = warningMessage;
	}
	public String getStatusTask() {
		return statusTask;
	}
	public void setStatusTask(String statusTask) {
		this.statusTask = statusTask;
	}  
	  
}
